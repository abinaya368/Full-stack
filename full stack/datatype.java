package programs;

public class datatype {

	public static void main(String[] args) {
		int age = 27;
		char initial = 'R';
		float f = 10.2586f;
		double d = 25.1234897;
		
		System.out.println(age);
		System.out.println(initial);
		System.out.println(f);
		System.out.println(d);
	}
}
